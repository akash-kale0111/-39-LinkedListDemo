import java.util.LinkedList;

public class LinkedListDemo1 {
	
public static void main(String[] args) {
		
		LinkedList<Integer> l = new LinkedList<Integer>(); //Here we Integer 
		
		l.add(14);
		l.add(45);
		l.add(85);
		l.add(98);
		
		for (int u: l) {
			System.out.println(u);  //for each loop
		
		
		}
}

}
